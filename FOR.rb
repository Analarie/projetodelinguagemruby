  # Exemplo 2: Calcular a soma dos números de 1 a 10 usando um loop 'for'
  sum = 0
  for num in 1..10
    # Loop 'for' que itera sobre os números de 1 a 10.
  
    # A variável 'num' representa o número atual do loop.
    # Em cada iteração, o número é adicionado à variável 'sum' para calcular a soma.
    sum += num
  end
  
  # Imprime a soma dos números de 1 a 10.
  puts "A soma dos números de 1 a 10 é #{sum}."
  
